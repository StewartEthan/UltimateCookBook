    private void addIngButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addIngButtonActionPerformed
         // Get the value from the text field
        String ingToAdd = addIngTextField.getText();
        
        // If the value exists, add it to the list of ingredients 
        // and clear the text field
        if (ingToAdd != null && ingToAdd.length() > 0) {
            ((DefaultListModel) editIngList.getModel()).addElement(ingToAdd);
            addIngTextField.setText("");
        }
    }//GEN-LAST:event_addIngButtonActionPerformed

