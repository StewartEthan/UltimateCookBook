    private void editOKButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editOKButtonActionPerformed
        // Get the new values from the inputs
        String newTitle = editTitleTextField.getText();
        String newDirections = jTextArea1.getText();
        String[] newIngredients = new String[editIngList.getModel().getSize()];
        for (int i = 0; i < newIngredients.length; i++) {
            newIngredients[i] = (String) editIngList.getModel().getElementAt(i);
        }

        // Ensure no components are empty
        boolean canUpdate = true;
        if (newTitle == null || newTitle.length() <= 0) {
            this.setEnabled(false);
            new ErrorMessageUI("Title cannot\nbe empty", "Edit Recipe");
            canUpdate = false;
        }
        
        if (newDirections == null || newDirections.length() <= 0) {
            this.setEnabled(false);
            new ErrorMessageUI("Directions cannot\nbe empty", "Edit Recipe");
            canUpdate = false;
        }
        
        if (newIngredients.length <= 0) {
            this.setEnabled(false);
            new ErrorMessageUI("You must have at\nleast one ingredient", "Edit Recipe");
            canUpdate = false;
        }
        
        for (String ing : newIngredients) {
            if (ing == null || ing.length() <= 0) {
                this.setEnabled(false);
                new ErrorMessageUI("Blank ingredients\nnot permitted", "Edit Recipe");
                canUpdate = false;
            }
        }
        
        // If canCreate is still true, update the recipe
        if (canUpdate) {
            // Write the recipe to file
            Recipe r = new Recipe(newTitle, newIngredients, newDirections);
            r.writeRecipeToFile();
            
            // Update the recipe list in the main window
            for (Frame frame : Frame.getFrames()) {
                if (frame.getTitle().equals("Kitty's Cookbook")) {
                    ((UltimateCookBookUI) frame).addRecipesToViewList(Recipe.readAllRecipes());
                    ((UltimateCookBookUI) frame).setAllRecipes(Recipe.readAllRecipes());
                    ((UltimateCookBookUI) frame).resetRecipeViewer();
                    break;
                }
            }
            
            // Re-enable main window and close this one
            closeSecondaryFrame();
        }
    }//GEN-LAST:event_editOKButtonActionPerformed

